def category_for(title: str) -> str:
    if title.startswith("Кровать"):
        return "Кровати"
    if title.startswith("Комплект"):
        return "Комплекты"
    if title.startswith("Кресло") and "," in title:
        return "Кресла"
    if title.startswith("Кресло"):
        return "Кресла"
    if title.startswith("Пуф"):
        return "Пуфы"
    if title.startswith("Банкетка"):
        return "Банкетки"
    if title.startswith("Софа"):
        return "Софы"
    return "Диваны"


def main() -> None:
    for pid, title in sorted(TITLES.items()):
        path = ITEMS / f"{pid}.json"
        if not path.is_file():
            continue
        data = json.loads(path.read_text(encoding="utf-8"))
        data["name"] = title
        data["category"] = category_for(title)
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(pid, "->", title)


if __name__ == "__main__":
    main()
