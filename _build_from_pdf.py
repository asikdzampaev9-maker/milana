def split_pages():
    os.makedirs(OUT_IMG, exist_ok=True)
    files = sorted(glob.glob(os.path.join(SRC, "p*.jpeg")))
    for fp in files:
        m = re.search(r"p(\d+)_", os.path.basename(fp))
        if not m:
            continue
        page = int(m.group(1))
        if page < 3:
            continue
        im = Image.open(fp).convert("RGB")
        w, h = im.size
        band = h // 3
        for i in range(3):
            y1 = i * band
            y2 = (i + 1) * band if i < 2 else h
            crop = im.crop((0, y1, w, y2))
            out_path = os.path.join(OUT_IMG, f"p{page:02d}-{i + 1}.jpg")
            crop.save(out_path, quality=90, optimize=True)


def default_item(pid, page, row):
    return {
        "id": pid,
        "name": f"Модель каталога (стр. {page}, блок {row})",
        "category": "Milana Group",
        "short": "Фрагмент страницы PDF-каталога 2026 — фото и таблица характеристик как в оригинале.",
        "description": "Изображение — точная вырезка со страницы каталога Milana Group. Все подписи и размеры на фото соответствуют печатному каталогу.",
        "specs": {
            "Источник": f"Каталог Milana Group 2026, стр. {page}",
        },
    }


def main():
    split_pages()
    for old in glob.glob(os.path.join(ITEMS, "*.json")):
        os.remove(old)

    ids = []
    for page in range(3, 31):
        for row in range(1, 4):
            pid = f"p{page:02d}-{row}"
            ids.append(pid)
            rel_img = f"data/images/products/{pid}.jpg"
            if pid in SPECS:
                data = dict(SPECS[pid])
            else:
                data = default_item(pid, page, row)
            data["image"] = rel_img
            with open(os.path.join(ITEMS, f"{pid}.json"), "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=2)

    catalog = {
        "brand": "Milana Group",
        "year": 2026,
        "source": "PDF-каталог на рабочем столе",
        "items": ids,
    }
    with open(os.path.join(ROOT, "data", "catalog.json"), "w", encoding="utf-8") as f:
        json.dump(catalog, f, ensure_ascii=False, indent=2)
    print("items", len(ids), "images", OUT_IMG)


if __name__ == "__main__":
    main()
